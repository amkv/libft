#include "libft.h"
#include <stdio.h>

int	main(void)
{
	char *src;
	char dst[0];

	src = "test";
	ft_strcpy(dst, src);
	printf("%s\n", dst);
	return (0);
}

char *ft_strcpy(char *dst, const char *src)
{
	unsigned int i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
